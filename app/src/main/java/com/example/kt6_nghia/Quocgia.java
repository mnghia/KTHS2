package com.example.kt6_nghia;

public class Quocgia {
    int logo;
    String ten;
    String danso;

    public Quocgia(int logo, String ten, String danso) {
        this.logo = logo;
        this.ten = ten;
        this.danso = danso;
    }

    public int getLogo() {
        return logo;
    }

    public void setLogo(int logo) {
        this.logo = logo;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getDanso() {
        return danso;
    }

    public void setDanso(String danso) {
        this.danso = danso;
    }
}
